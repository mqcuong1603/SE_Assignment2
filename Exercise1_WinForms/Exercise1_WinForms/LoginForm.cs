        private void LoginForm_Load(object sender, EventArgs e)
        {
            if (!userBLL.TestDatabaseConnection())
            {
                MessageBox.Show("Cannot connect to database. Please check your connection string.", "Database Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

        private void btnLogin_Click(object sender, EventArgs e)
        {
            try
            {
                string username = txtUsername.Text.Trim();
                string password = txtPassword.Text;

                User user = userBLL.Login(username, password);
                SessionManager.CurrentUser = user;

                MessageBox.Show($"Welcome {user.UserName}!", "Login Successful", MessageBoxButtons.OK, MessageBoxIcon.Information);

                MainForm mainForm = new MainForm();
                mainForm.Show();
                this.Hide();
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message, "Login Failed", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

        private void btnExit_Click(object sender, EventArgs e)
        {
            Application.Exit();
        }
